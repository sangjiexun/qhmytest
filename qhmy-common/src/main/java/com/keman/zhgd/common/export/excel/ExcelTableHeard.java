/**
 * 表头实例对象
 * 记录到
 * @author Administrator
 *
 */
public class ExcelTableHeard {
	
	/**
	 * 开始行
	 */
	private int startH;
	
	/**
	 * 结束行
	 */
	private int endH;
	
	/**
	 * 开始列
	 */
	private int startL;
	
	/**
	 * 结束列
	 */
	private int endL;

	public int getStartH() {
		return startH;
	}

	public void setStartH(int startH) {
		this.startH = startH;
	}

	public int getEndH() {
		return endH;
	}

	public void setEndH(int endH) {
		this.endH = endH;
	}

	public int getStartL() {
		return startL;
	}

	public void setStartL(int startL) {
		this.startL = startL;
	}

	public int getEndL() {
		return endL;
	}

	public void setEndL(int endL) {
		this.endL = endL;
	}
	
	
	
}
